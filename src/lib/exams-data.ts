// ===== Question types =====

export type MultiQuestion = {
  type: "multi";
  q: string;
  options: string[];
  correct: number;
};

export type OpenQuestion = {
  type: "open";
  q: string;
  sample: string; // model answer shown after submission
  rubric: string; // grading criteria for Claude
};

export type FillQuestion = {
  type: "fill";
  q: string; // contains {{0}}, {{1}} placeholders
  blanks: string[][]; // accepted variants per blank
};

export type TFQuestion = {
  type: "tf";
  q: string;
  correct: boolean;
  explanation: string; // shown after submission
};

export type MatchQuestion = {
  type: "match";
  q: string;
  left: string[]; // displayed in fixed order
  right: string[]; // matching order: right[i] matches left[i]; UI will shuffle
};

export type Question =
  | MultiQuestion
  | OpenQuestion
  | FillQuestion
  | TFQuestion
  | MatchQuestion;

export type Sugiya = {
  id: number;
  title: string;
  pdfs: string[];
  questions: Question[];
};

export type Exam = {
  id: string;
  title: string;
  subtitle: string;
  pages: string;
  examDate: string;
  sugiyot: Sugiya[];
};

